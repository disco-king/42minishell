#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	m;
	int	n;

	n = 0;
	i = 0;
	m = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == ' '))
	{
		i++;
	}
	if (str[i] == '-')
	{
		m *= -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while ((str[i] >= '0' && str[i] <= '9') && str[i] != '\0')
	{
		n = n * 10 + (str[i] - 48);
		i++;
	}
	return (m * n);
}
